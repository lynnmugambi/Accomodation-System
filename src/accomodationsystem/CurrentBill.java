    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        // TODO add your handling code here:
        if (meternumtxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Meter Number Field is empty!");
        }
        
        else{
        mf.searchmeter(Integer.parseInt(meternumtxt.getText()));

        namelbl.setText(mf.getMetername());
        typelbl.setText(mf.getMetertype());
        usagelbl.setText(Integer.toString(mf.getElecusage()));
        statuslbl.setText(mf.getStatus());
        
        acc.setMeternum(Integer.parseInt(meternumtxt.getText()));
        acc.setElecusage(Integer.parseInt(usagelbl.getText()));
        acc.setcost(typelbl.getText());
        System.out.println("I got this far");
        }
    }//GEN-LAST:event_searchbtnActionPerformed

