    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if ("Post-pay".equals(typelbl.getText())) {
            acc.setMeternum(Integer.parseInt(meternumtxt.getText()));
            acc.getpostcost();           
            costinclbl.setText(Integer.toString(acc.getCost()));
      
            if ((Integer.parseInt(usagelbl.getText()) >= 1000)) {
                acc.suspend(Integer.parseInt(meternumtxt.getText()));
            }
        }
        if ("Pre-pay".equals(typelbl.getText())){
            acc.getprecost(Integer.parseInt(meternumtxt.getText()), typelbl.getText());
            balancelbl.setText(Integer.toString(acc.getCost()));
            
            if ((Integer.parseInt(balancelbl.getText())) < 1) {
                acc.suspend(Integer.parseInt(meternumtxt.getText()));
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

